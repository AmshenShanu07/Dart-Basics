void main() {
  print("Its a For Loop");
  for (int i = 0; i < 5; i++) {
    print("Amshen");
  }
  print('------------------\n');
  print('Its a While Loop\n');

  int i = 5;

  while (i > 0) {
    print("Amshen");
    i--;
  }

  print('------------------\n');
  print('Its a Do While Loop\n');

  i = 5;

  do {
    print("Amshen");
    i--;
  } while (i > 0);
}
